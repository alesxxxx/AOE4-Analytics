/**
 * Vendors the overlay's unit + building icons from data.aoe4world.com so
 * nothing is fetched mid-match (instant + offline; see NOTICE for the asset
 * terms). Sources of truth for WHICH icons: every `icon` slug in
 * src/domain/civUnits.ts (troop cheat-sheet) plus every `b('…')`/`u('…')`
 * slug in src/renderer/overlay/buildIcons.ts (build-order widget). Tries the
 * same suffix candidates the UI's self-heal used (`-<age>`, `-1`..`-4`, none),
 * saves hits under src/data/vendor/aoe4world-overlay/{units,buildings}/, and
 * regenerates the units.ts / buildings.ts manifests.
 *
 *   node scripts/vendor-unit-icons.mjs
 */
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'

const ROOT = path.resolve(import.meta.dirname, '..')
const CIV_UNITS = path.join(ROOT, 'src', 'domain', 'civUnits.ts')
const BUILD_ICONS = path.join(ROOT, 'src', 'renderer', 'overlay', 'buildIcons.ts')
const VENDOR_DIR = path.join(ROOT, 'src', 'data', 'vendor', 'aoe4world-overlay')
const CDN = 'https://data.aoe4world.com/images'

const civUnitsSrc = await readFile(CIV_UNITS, 'utf8')
const buildIconsSrc = await readFile(BUILD_ICONS, 'utf8')

// Every `{ name: …, icon: '<slug>', age: <n> }` entry; a slug can appear with
// several ages across civs — collect all ages per slug for the candidate order.
const unitAges = new Map()
for (const m of civUnitsSrc.matchAll(/icon:\s*'([a-z0-9-]+)'\s*,\s*age:\s*([234])/g)) {
  const [, slug, age] = m
  if (!unitAges.has(slug)) unitAges.set(slug, new Set())
  unitAges.get(slug).add(age)
}
if (unitAges.size === 0) throw new Error('No icon slugs found in civUnits.ts — regex out of date?')

// Build-order widget slugs: u('…') are units, b('…') are buildings.
for (const m of buildIconsSrc.matchAll(/\bu\('([a-z0-9-]+)'\)/g)) {
  if (!unitAges.has(m[1])) unitAges.set(m[1], new Set())
}
const buildingSlugs = new Set()
for (const m of buildIconsSrc.matchAll(/\bb\('([a-z0-9-]+)'\)/g)) buildingSlugs.add(m[1])
if (buildingSlugs.size === 0)
  throw new Error('No building slugs found in buildIcons.ts — regex out of date?')

/** Downloads each slug's first resolving CDN candidate into `dir`. */
async function vendor(kind, slugs, agesBySlug) {
  const dir = path.join(VENDOR_DIR, kind)
  await mkdir(dir, { recursive: true })
  const resolved = []
  const missing = []
  for (const slug of [...slugs].sort()) {
    const ages = [...(agesBySlug?.get(slug) ?? [])].map((a) => `-${a}`)
    const candidates = [...new Set([...ages, '-1', '-2', '-3', '-4', ''])]
    let saved = false
    for (const suffix of candidates) {
      const res = await fetch(`${CDN}/${kind}/${slug}${suffix}.png`)
      if (!res.ok) continue
      const buf = Buffer.from(await res.arrayBuffer())
      await writeFile(path.join(dir, `${slug}.png`), buf)
      console.log(`${kind}/${slug}.png  <-  ${slug}${suffix}.png (${(buf.length / 1024).toFixed(1)} KB)`)
      resolved.push(slug)
      saved = true
      break
    }
    if (!saved) {
      console.warn(`MISS ${kind}/${slug} — no CDN candidate resolved`)
      missing.push(slug)
    }
  }
  return { resolved, missing }
}

const ident = (slug) => slug.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase())

function manifest(kind, exportName, doc, resolved) {
  return `/**
 * ${doc}, vendored from data.aoe4world.com (see
 * SOURCE.md + NOTICE for terms). Generated by scripts/vendor-unit-icons.mjs —
 * do not edit by hand. Static imports → Vite resolves each to a hashed asset
 * URL string.
 */
${resolved.map((s) => `import ${ident(s)} from './${kind}/${s}.png'`).join('\n')}

/** Icon slug → bundled image URL. */
export const ${exportName}: Record<string, string> = {
${resolved.map((s) => `  '${s}': ${ident(s)},`).join('\n')}
}
`
}

const units = await vendor('units', unitAges.keys(), unitAges)
await writeFile(
  path.join(VENDOR_DIR, 'units.ts'),
  manifest('units', 'UNIT_ICONS', 'Overlay unit icons (troop sheet + build-order widget)', units.resolved),
)
const buildings = await vendor('buildings', buildingSlugs)
await writeFile(
  path.join(VENDOR_DIR, 'buildings.ts'),
  manifest('buildings', 'BUILDING_ICONS', 'Overlay build-order building icons', buildings.resolved),
)

const missing = [...units.missing, ...buildings.missing]
console.log(
  `\n${units.resolved.length} units + ${buildings.resolved.length} buildings vendored -> manifests written`,
)
if (missing.length) console.log(`Missing (UI keeps its CDN fallback): ${missing.join(', ')}`)
